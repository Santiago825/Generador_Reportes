public class LiderVo {
    
    private int id;
    private String nombre;
    private String primerApellido;
    private String ciudadResidencial;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPrimerApellido() {
        return primerApellido;
    }

    public void setPrimerApellido(String primerApellido) {
        this.primerApellido = primerApellido;
    }

    public String getCiudadResidencial() {
        return ciudadResidencial;
    }

    public void setCiudadResidencial(String ciudadResidencial) {
        this.ciudadResidencial = ciudadResidencial;
    }
    
    
    
    
}
