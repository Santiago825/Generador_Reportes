public class ProyectoVo {
    private int id;
    private String constructora;
    private String numero_habitantes;
    private String ciudad;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getConstructora() {
        return constructora;
    }

    public void setConstructora(String constructora) {
        this.constructora = constructora;
    }

    public String getNumero_habitantes() {
        return numero_habitantes;
    }

    public void setNumero_habitantes(String numero_habitantes) {
        this.numero_habitantes = numero_habitantes;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }
    
    
    
    
}
